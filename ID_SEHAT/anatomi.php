                <h2>Anatomi Tubuh Manusia</h2><br>
                <hr>
                <h3>1. Sistem Rangka</h3>
                <img src="assets/anatomi/rangka.jpg">
                <p>Tubuh manusia didukung oleh sistem rangka, yang terdiri dari 206 tulang yang dihubungkan oleh tendon,
                    ligamen, dan tulang rawan. Tulang ini disusun oleh kerangka aksial dan kerangka apendikular.</p>
                <p>Kerangka aksial terdiri dari 80 tulang yang terletak di sepanjang sumbu tubuh manusia. Kerangka
                    aksial terdiri dari tengkorak, tulang telinga tengah, tulang hyoid, tulang rusuk, dan tulang
                    belakang.</p>
                <p>Kerangka apendikular terdiri dari 126 tulang yang merupakan tulang-tulang pelengkap yang
                    menghubungkan kerangka aksial. Kerangka apendikular terletak di daerah tungkai atas, tungkai bawah,
                    panggul, dan bahu.</p>
                <p>Fungsi sistem rangka untuk bergerak, menopang dan memberikan bentuk tubuh, melindungi organ-organ
                    dalam, serta sebagai tempat melekatnya otot-otot.</p>
                <hr>
                <h3>2. Sistem Otot</h3>
                <img src="assets/anatomi/otot.jpg">
                <p>Sistem otot terdiri dari sekitar 650 otot yang membantu pergerakan, aliran darah, dan fungsi tubuh
                    lainnya. Ada tiga jenis otot yaitu otot rangka yang terhubung dengan tulang, otot polos yang
                    ditemukan di dalam organ pencernaan, dan otot jantung yang ditemukan di jantung dan membantu memompa
                    darah.</p>
                <hr>
                <h3>3. Sistem Peredaran Darah</h3>
                <img src="assets/anatomi/darah.jpg">
                <p>Sistem peredaran darah terdiri dari jantung, pembuluh darah, dan sekitar 5 liter darah yang dibawa
                    oleh pembuluh darah. Sistem peredaran darah didukung oleh jantung, yang hanya seukuran kepalan
                    tangan tertutup. Bahkan pada saat istirahat, rata-rata jantung dengan mudah memompa lebih dari 5
                    liter darah ke seluruh tubuh setiap menitnya. Sistem peredaran darah memiliki tiga fungsi utama
                    yaitu:</p>
                <ul>
                    <li>
                        <p><span style="font-weight: 500;">Mengedarkan darah ke seluruh tubuh.</span> Darah memberikan
                            nutrisi penting dan oksigen dan menghilangkan limbah dan karbon dioksida untuk dikeluarkan
                            dari tubuh. Hormon diangkut ke seluruh tubuh melalui cairan plasma darah.</p>
                    </li>
                    <li>
                        <p><span style="font-weight: 500;">Melindungi tubuh melalui sel darah putih dengan melawan
                                patogen (kuman) yang telah masuk ke dalam tubuh.</span> Trombosit berfungsi untuk
                            menghentikan perdarahan saat luka dan mencegah patogen memasuki tubuh. Darah juga membawa
                            antibodi yang memberi kekebalan spesifik pada patogen yang sebelumnya telah terpapar tubuh
                            atau telah divaksinasi.</p>
                    </li>
                    <li>
                        <p><span style="font-weight: 500;">Mempertahankan homeostasis (keseimbangan kondisi tubuh) pada
                                beberapa kondisi internal.</span> Pembuluh darah membantu menjaga suhu tubuh yang stabil
                            dengan mengendalikan aliran darah ke permukaan kulit.</p>
                    </li>
                </ul>
                <hr>
                <h3>4. Sistem Pencernaan</h3>
                <img src="assets/anatomi/pencernaan.jpg">
                <p>Sistem pencernaan adalah sekelompok organ yang bekerja untuk menerima makanan, mengubah dan memproses
                    makanan menjadi energi, menyerap zat gizi yang terdapat pada makanan ke aliran darah, serta membuang
                    sisa makanan yang tersisa atau tidak dapat dicerna oleh tubuh.</p>
                <p>Makanan melewati saluran pencernaan yang terdiri dari rongga mulut, faring (tenggorokan), laring
                    (kerongkongan), lambung, usus halus, usus besar, dan berakhir di anus.</p>
                <p>Selain saluran pencernaan, ada beberapa organ aksesori penting dalam anatomi tubuh manusia yang
                    membantu mencerna makanan. Organ aksesori dari sistem pencernaan meliputi gigi, lidah, kelenjar
                    ludah, hati, kantong empedu, dan pankreas.</p>
                <hr>
                <h3>5. Sistem Endokrin</h3>
                <img src="assets/anatomi/endokrin.jpg">
                <p>Sistem endokrin terdiri dari beberapa kelenjar yang mengeluarkan hormon ke dalam darah.
                    Kelenjar-kelenjar ini termasuk hipotalamus, kelenjar pituitari, kelenjar pineal, kelenjar tiroid,
                    kelenjar paratiroid, kelenjar adrenal, pankreas, dan kelenjar kelamin (gonad).</p>
                <p>Kelenjar dikendalikan secara langsung oleh rangsangan dari sistem saraf dan juga oleh reseptor
                    kimiawi dalam darah dan hormon yang diproduksi oleh kelenjar lain. Dengan mengatur fungsi organ
                    dalam tubuh, kelenjar ini membantu menjaga homeostasis tubuh. Metabolisme seluler, reproduksi,
                    perkembangan seksual, homeostasis gula dan mineral, denyut jantung, dan pencernaan merupakan salah
                    satu dari banyak proses yang diatur oleh hormon.</p>
                <hr>
                <h3>6. Sistem Saraf</h3>
                <img src="assets/anatomi/saraf.jpg">
                <p>Sistem saraf terdiri dari otak, sumsum tulang belakang, organ sensorik, dan semua saraf yang
                    menghubungkan organ-organ ini dengan bagian tubuh lainnya. Organ-organ ini bertanggung jawab atas
                    kendali tubuh dan komunikasi di antara bagian-bagiannya.</p>
                <p>Otak dan sumsum tulang belakang membentuk pusat kontrol yang dikenal sebagai sistem saraf pusat.
                    Saraf sensorik dan organ indra dari sistem saraf perifer memantau kondisi di dalam dan di luar tubuh
                    dan mengirimkan informasi sistem saraf pusat. Saraf eferen di sistem saraf perifer membawa sinyal
                    dari pusat kendali ke otot, kelenjar, dan organ untuk mengatur fungsinya.</p>
                <hr>
                <h3>7. Sistem Pernapasan</h3>
                <img src="assets/anatomi/pernapasan.jpg">
                <p>Sel-sel tubuh manusia membutuhkan aliran oksigen untuk tetap hidup. Sistem pernapasan menyediakan
                    oksigen ke sel tubuh sambil mengeluarkan karbon dioksida dan produk limbah yang bisa mematikan jika
                    dibiarkan menumpuk.</p>
                <p>Ada tiga bagian utama dari sistem pernapasan: saluran napas, paru-paru, dan otot-otot respirasi.
                    Saluran napas meliputi hidung, mulut, faring, laring, trakea, bronkus, dan bronkiolus. Saluran ini
                    membawa udara melewati hidung menuju paru-paru.</p>
                <p>Paru-paru berfungsi sebagai organ utama sistem pernapasan dengan pertukaran oksigen ke dalam tubuh
                    dan karbon dioksida keluar dari tubuh. Otot respirasi, termasuk diafragma dan otot interkostal,
                    bekerja sama untuk memompa, mendorong udara masuk dan keluar dari paru-paru saat bernapas.</p>
                <hr>
                <h3>8. Sistem Kekebalan Tubuh</h3>
                <img src="assets/anatomi/kekebalan.jpg">
                <p>Sistem kekebalan tubuh adalah pertahanan tubuh terhadap bakteri, virus, dan patogen lainnya yang
                    mungkin berbahaya, dengan menjaga dan menyerang dari patogen-patogen tersebut. Ini termasuk kelenjar
                    getah bening, limpa, sumsum tulang, limfosit (termasuk sel B dan sel T), timus, dan leukosit, yang
                    merupakan sel darah putih.</p>
                <hr>
                <h3>9. Sistem Limfatik</h3>
                <img src="assets/anatomi/limfatik.jpg">
                <p>Dalam anatomi tubuh mansia, sistem limfatik mencakup kelenjar getah bening, saluran getah bening, dan
                    pembuluh getah bening, dan juga berperan dalam pertahanan tubuh. Tugas utamanya adalah membuat dan
                    memindahkan getah bening, cairan bening yang mengandung sel darah putih, yang membantu tubuh melawan
                    infeksi. Sistem limfatik juga menghilangkan kelebihan cairan getah bening dari jaringan tubuh, dan
                    mengembalikannya ke darah.</p>
                <hr>
                <h3>10. Sistem Ekskresi dan Urinaria</h3>
                <img src="assets/anatomi/urinaria.jpg">
                <p>Sistem ekskresi mengeluarkan zat sisa yang tidak dibutuhkan lagi oleh manusia. Pada anatomi tubuh
                    manusia, organ-organ ekskresi terdiri dari ginjal, hati, kulit, dan paru-paru.</p>
                <p>Sistem urinaria atau perkemihan termasuk ke dalam sistem eksresi yang terdiri dari ginjal, ureter,
                    kandung kemih, dan uretra. Ginjal menyaring darah untuk membuang limbah dan menghasilkan urine.
                    Ureter, kandung kemih, dan uretra bersama-sama membentuk saluran kemih, yang berfungsi sebagai
                    sistem untuk mengalirkan urine dari ginjal, menyimpannya, dan kemudian melepaskannya saat buang air
                    kecil.</p>
                <p>Selain menyaring dan menghilangkan limbah dari tubuh, sistem urinaria juga mempertahankan homeostasis
                    air, ion, pH, tekanan darah, kalsium, dan sel darah merah.</p>
                <p>Organ hati berfungsi mengeluarkan empedu, kulit berfungsi mengeluarkan keringat, sedangkan paru-paru
                    berfungsi dalam mengeluarkan uap air dan karbon dioksida.</p>
                <hr>
                <h3>11. Sistem Reproduksi</h3>
                <img src="assets/anatomi/reproduksi.jpg">
                <p>Sistem reproduksi memungkinkan manusia untuk bereproduksi. Sistem reproduksi pria mencakup penis dan
                    testis, yang menghasilkan sperma.</p>
                <p>Sistem reproduksi wanita terdiri dari vagina, rahim dan ovarium, yang menghasilkan ovum (sel telur).
                    Selama pembuahan, sel sperma bertemu dengan sel telur di tuba falopi. Kedua sel tersebut kemudian
                    melakukan pembuahan yang ditanamkan dan tumbuh di dinding rahim. Bila tidak dibuahi, dinding rahim
                    yang telah menebal untuk mempersiapkan kehamilan akan luruh menjadi menstruasi.</p>
                <hr>
                <h3>12. Sistem Integumen</h3>
                <img src="assets/anatomi/integumen.png">
                <p>Kulit atau sistem integumen adalah organ terbesar dalam anatomi tubuh manusia. Sistem ini melindungi
                    dari dunia luar, dan merupakan pertahanan pertama tubuh melawan bakteri, virus dan patogen lainnya.
                    Kulit juga membantu mengatur suhu tubuh dan menghilangkan limbah zat sisa melalui keringat. Selain
                    kulit, sistem integumen meliputi rambut dan kuku.</p>
